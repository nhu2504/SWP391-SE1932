    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        Object userIdObj = request.getSession().getAttribute("userId");
    if (userIdObj == null) {
        // Chưa đăng nhập hoặc session hết hạn, chuyển về trang đăng nhập
        response.sendRedirect("login_register.jsp");
        return;
    }
    int userId = Integer.parseInt(userIdObj.toString());
        ScheduleDAO dao = new ScheduleDAO();
        ScheduleJoin nextSchedule = null;
        try {
            nextSchedule = dao.getNextScheduleByTeacher(userId);
        } catch (SQLException e) {
            e.printStackTrace();
        }
        request.setAttribute("nextSchedule", nextSchedule);
        request.getRequestDispatcher("teacherdashboard.jsp").forward(request, response);
    }
    

